                The local GBC man, when questioned, acknowledged that a contract had been signed but said that New York State “confidentiality laws” prevented him from saying anything more about it. Who was the buyer? What was the price? Where would Sri Sri Radha Govinda move to and when? No one was telling. And still this information is being kept secret. 
                The GBC body was left uninformed that a specific contract of sale had been negotiated until more than three months after it was signed and members of the temple congregation informed them.
                For the sale of a religious building in New York to go through, the sellers need the approval of the New York State Attorney General. So members of the temple congregation quickly wrote to the Attorney General to ask him to withhold permission. When the Attorney General withholds permission, generally the seller is obligated to turn to the local court to ask permission. If the court refuses, contract is dissolved.
